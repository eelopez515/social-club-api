const mongoose = require('mongoose')
const likesSchema = require('./likes')
const pictureSchema = require('./pictures')

const userSchema = new mongoose.Schema({
  firstName: {
    type: String,
    required: true
  },
  lastName: {
    type: String,
    required: true
  },
  zipcode: {
    type: Number,
    required: true,
    min: 5
  },
  gender: {
    type: String
  },
  email: {
    type: String,
    required: true,
    unique: true
  },
  hashedPassword: {
    type: String,
    required: true
  },
  likes: [likesSchema],
  pictures: [pictureSchema],
  token: String
}, {
  timestamps: true,
  toObject: {
    // remove `hashedPassword` field when we call `.toObject`
    transform: (_doc, user) => {
      delete user.hashedPassword
      return user
    }
  }
})

module.exports = mongoose.model('User', userSchema)
